// Current language
let currentLanguage = localStorage.getItem('selectedLanguage') || 'en';

// Function to translate text
function translateText(text, lang = currentLanguage) {
  if (translations[lang] && translations[lang][text]) {
    return translations[lang][text];
  }
  // Fallback to English if translation not found
  return translations.en[text] || text;
}

// Function to translate entire page
function translatePage(lang = currentLanguage) {
  currentLanguage = lang;
  localStorage.setItem('selectedLanguage', lang);

  // Load Arabic font for Urdu if not already loaded
  if (lang === 'ur' && !document.querySelector('link[href*="Noto+Sans+Arabic"]')) {
    const link = document.createElement('link');
    link.href = 'https://fonts.googleapis.com/css2?family=Noto+Sans+Arabic:wght@400;700&display=swap';
    link.rel = 'stylesheet';
    document.head.appendChild(link);
  }

  // Apply font switching based on language
  if (lang === 'ur') {
    document.body.style.fontFamily = "'Noto Sans Arabic', serif";
    document.body.style.direction = 'rtl'; // Right-to-left for Urdu
  } else {
    document.body.style.fontFamily = "'Inter', sans-serif";
    document.body.style.direction = 'ltr'; // Left-to-right for English and Roman Urdu
  }

  // Translate all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    element.textContent = translateText(key, lang);
  });

  // Translate placeholders
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    element.placeholder = translateText(key, lang);
  });

  // Translate select options
  document.querySelectorAll('[data-translate-option]').forEach(element => {
    const key = element.getAttribute('data-translate-option');
    element.textContent = translateText(key, lang);
  });

  // Update language selector
  const languageSelect = document.getElementById('languageSelect');
  if (languageSelect) {
    languageSelect.value = lang;
  }

  // Special handling for dynamic content
  updateDynamicTranslations(lang);
}

// Function to update dynamic content translations
function updateDynamicTranslations(lang) {
  // Update profile completion text
  const progressText = document.getElementById('progressText');
  if (progressText) {
    const percentage = progressText.textContent.split('%')[0];
    progressText.textContent = `${percentage}% ${translateText('Complete', lang)}`;
  }

  // Update spiritual note
  const spiritualNote = document.querySelector('.spiritual-note');
  if (spiritualNote) {
    spiritualNote.textContent = translateText('spiritual_note', lang);
  }
}

